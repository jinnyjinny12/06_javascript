var lastName = '홍';
var firstName = '길동';

// 문자열은 문자열 연산자 +를 사용해 연결할 수 있다.
console.log('제 이름은 ' + lastName + firstName + '입니다.');

// 표현식 삽입을 이용하면 문자열 연산자보다 가독성 좋고 간편하게 문자열을 조합할 수 있다. 
console.log(`제 이름은 ${lastName}${firstName}입니다.`);

// // **백틱 표기법
// 1. 여러줄로 이루어진 문자열 : 백틱을 사용하면 여러 줄로 이루어진 문자열을 쉽게 작성할 수 있다. 
// `\n` 을 사용하지 않아도 여러줄로 이루어진 문자열을 자연스럽게 작성할 수 있다. 

// 2. 문자열 보간(Interpolation)
// 백틱 안에 '${변수명}' 형식을 사용하여 변수를 문자열 안에 삽입할 수 있습니다. 변수의 값을
// 동적으로 문자열에 삽입할 때 유리하다.

// 3. 특수 문자 이스케이프의 필요성 감소
// 백틱을 사용하면 특수문자 이스케이프를 할 필요가 없다. 효율성 증가한다!

// 이스케이프
// 1)`\n` : 줄바꿈
// 2)`\t` : 탭 문자
// 3)`\"` : 큰 따옴표
// 4)`\` : 작은 따옴표
// 5) `\\` : 백슬래시 자체를 나타냄




// 표현식 삽입은 반드시 백틱 내에서 사용해야 한다.
// 일반 문자열에서의 표현식 삽입은 문자열로 취급된다.
console.log('제 이름은 ${lastName}${firstName}입니다.');