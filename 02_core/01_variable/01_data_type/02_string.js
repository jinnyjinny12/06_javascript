// console.log(multiline);

var multiline = `multiline2라고
줄이 바꿔질까요? 안되네요.`; // 이건 백틱 표기법을 사용한다. 백틱은 여러줄로 이루어진 문자열을 쉽게 표기할 때 사용.
console.log(multiline);

var multiline2 = `multiline2 라고
써도 됩니다! 오 신기하네요!`;
