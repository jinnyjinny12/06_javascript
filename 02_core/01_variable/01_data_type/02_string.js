var str = '저는 000입니다. 지금부터 줄바꿈 테스트를 진행할건데요! \n줄이 바꿔졌나요?';
console.log(str);

// 백틱을 사용하면 줄바꿈이 허용 되고 모든 공백이 있는 그대로 적용 된다.
// var multiline = `안녕하세요.
