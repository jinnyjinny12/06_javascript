/* 02. 문자열 타입 
문자열 타입은 텍스트 데이터를 나타내는데 사용한다.
문자열은 작은 따옴표(''), 큰 따옴표(""), 또는 백틱(``)으로 텍스트를 감싼다.
자바는 문자열을 객체로 표현하지만 자바스크립트의 문자열은 원시 타입이며, 변경 불가능한 값이다.
 */

var string;
string = 'JavaScript';  // 작은 따옴표
string = "JavaScript";  // 큰 따옴표
string = `JavaScript`;  // 백틱(ES6)
console.log(string); 
string = `JavaScript ${str}`; // 이런거도 가능함. 문자열 안에 변수 선언 가능.
console.log(string);  // javascript 를 출력하고 str변수가 선언되어 있지 않아 undefined로 나옴


string = '작은 따옴표로 감싼 문자열 내의 "큰 따옴표"는 문자열로 인식';
string = "큰 따옴표로 감싼 문자열 내의 '작은 따옴표'는 문자열로 인식";

/*
템플릿 리터럴
ES6부터 도입된 멀티라인 문자열, 표현식 삽입 등의 편리한 문자열 처리 기능을 제공하는 문자열 표기법이다.
이 때는 작은 따옴표, 큰 따옴표 대신 백틱을 사용해 표현한다.
*/

// 일반 문자열 내에서는 줄바꿈이 허용되지 않는다.


